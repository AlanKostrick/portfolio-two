//PROGRAMMER
// Get the modal
var programmerModal = document.getElementById('programmerModal');

// Get the button that opens the modal
var btnProgrammer = document.getElementById("programmer");

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close programmer")[0];

// When the user clicks the button, open the modal 
btnProgrammer.onclick = function() {
    programmerModal.style.display = "block";
}

// When the user clicks on <span> (x), close the modal
span.onclick = function() {
    programmerModal.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
    if (event.target == programmerModal) {
        programmerModal.style.display = "none";
    }
}



